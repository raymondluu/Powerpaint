package tools;

import java.awt.Color;
import java.awt.Point;
import java.awt.Shape;

/**
 * Tools Interface.
 * 
 * @author Raymond Luu
 * @version February 23, 2012
 */
public interface Tools
{ 
  
  /**
   * Returns the shape.
   * 
   * @return the shape.
   */
  Shape getShape();
  
  /**
   * Returns the shape string.
   * 
   * @return string.
   */
  String getShapeString();
  
  /**
   * Returns the color.
   * 
   * @return color.
   */
  Color getColor();
  
  /**
   * Returns the thickness.
   * 
   * @return integer thickness.
   */
  int getThickness();
  
  /**
   * Sets start point.
   * 
   * @param the_point a point.
   */
  void setStartPoint(Point the_point);
  
  /**
   * Sets end point.
   * 
   * @param the_point a point.
   */
  void setEndPoint(Point the_point);
  
  /**
   * Sets the shape.
   */
  void setShape();
  
  /**
   * Sets the color.
   * 
   * @param the_color color.
   */
  void setColor(Color the_color);
  
  /**
   * Set thickness.
   * 
   * @param the_thickness integer thickness.
   */
  void setThickness(int the_thickness);
  
  /**
   * Next tool.
   * 
   * @return another tool.
   */
  Tools nextTool();
}
